-- Create Table Statements

CREATE TABLE PostalCode_City_Province (
	PostalCode VARCHAR(256) PRIMARY KEY, 
	City VARCHAR(256) NOT NULL,
	Province VARCHAR(256) NOT NULL
);

CREATE TABLE Parliament (
	Country VARCHAR(256) PRIMARY KEY,
	StreetAddress VARCHAR(256) NOT NULL, 
	PostalCode VARCHAR(256) NOT NULL,
	FOREIGN KEY (PostalCode) REFERENCES PostalCode_City_Province (PostalCode) ON DELETE CASCADE
);

CREATE TABLE Party (
	PartyName VARCHAR(256) PRIMARY KEY,
	FoundingYear INTEGER NOT NULL,
	Incumbent INTEGER NOT NULL
);

CREATE TABLE Policy (
	Party_PartyName VARCHAR(256), 
	PolicyName VARCHAR(256), 
	Description VARCHAR(2048) NOT NULL, 
	PRIMARY KEY (Party_PartyName, PolicyName),
	FOREIGN KEY (Party_PartyName) REFERENCES Party ON DELETE CASCADE
);

CREATE TABLE Province (
	Name VARCHAR(256) PRIMARY KEY, 
	Premier VARCHAR(256)
);

CREATE TABLE Riding (
	ID INTEGER PRIMARY KEY, 
	Population INTEGER NOT NULL, 
	Name VARCHAR(256) UNIQUE NOT NULL, 
	Province_Name VARCHAR(256) NOT NULL,
	FOREIGN KEY (Province_Name) REFERENCES Province (Name) ON DELETE CASCADE
);

CREATE TABLE PollingStation (
	Name VARCHAR(256) PRIMARY KEY, 
	StreetAddress VARCHAR(256) NOT NULL, 
	PostalCode VARCHAR(256) NOT NULL, 
	Riding_ID INTEGER NOT NULL,
	FOREIGN KEY (Riding_ID) REFERENCES Riding (ID) ON DELETE CASCADE,
	FOREIGN KEY (PostalCode) REFERENCES PostalCode_City_Province (PostalCode) ON DELETE CASCADE
);

CREATE TABLE Voter (
	SIN INTEGER PRIMARY KEY, 
	Email VARCHAR(256) UNIQUE, 
	FullName VARCHAR(256) NOT NULL, 
	DOB VARCHAR(256) NOT NULL, 
	Voted INTEGER NOT NULL, 
	Party_PartyName VARCHAR(256),
	Role VARCHAR(256), 
	StreetAddress VARCHAR(256) NOT NULL, 
	PostalCode VARCHAR(256) NOT NULL,
	FOREIGN KEY (Party_PartyName) REFERENCES Party (PartyName) ON DELETE SET NULL,
	FOREIGN KEY (PostalCode) REFERENCES PostalCode_City_Province (PostalCode) ON DELETE CASCADE
);

CREATE TABLE Candidate (
	SIN INTEGER PRIMARY KEY, 
	Votes INTEGER NOT NULL, 
	Incumbent INTEGER NOT NULL, 
	Parliament_Country VARCHAR(256), 
	Riding_ID INTEGER NOT NULL,
	FOREIGN KEY (SIN) REFERENCES VOTER (SIN),
	FOREIGN KEY (Parliament_Country) REFERENCES Parliament (Country) ON DELETE SET NULL,
	FOREIGN KEY (Riding_ID) REFERENCES Riding (ID) ON DELETE CASCADE
);

CREATE TABLE Ballot (
	ID INTEGER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY, 
	Choice VARCHAR(256), 
	Voter_SIN INTEGER UNIQUE, 
	PollingStation_Name VARCHAR(256),
	FOREIGN KEY (Voter_SIN) REFERENCES Voter (SIN) ON DELETE CASCADE,
	FOREIGN KEY (PollingStation_Name) REFERENCES PollingStation (Name) ON DELETE CASCADE
);

